var result;
function functionElse(num) {
    if (num <= 5) {
        return result = "5 or smaller";
    } else {
        return result = "Bigger than 5";
    }
}
console.log(functionElse(4));
console.log(functionElse(5));
console.log(functionElse(6));
console.log(functionElse(10));
